n = int(input("Digite um número e veja sua tabuada:"))
print("==Adição==")
for num in range(1,11):
    print(f"{n} + {num} = {n + num}")
print("==Subtração==")
for num in range(1,11):
    print(f"{n} - {num} = {n - num}")
print("==Multiplicação==")
for num in range(1,11):
    print(f"{n} x {num} = {n * num}")
print("==Subtração==")
for num in range(1,11):
    print(f"{n} / {num} = {n / num}")




